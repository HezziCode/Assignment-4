def triangle_perimeter():
    top_side = float(input("Enter the top side of the triangle: "))
    right_side = float(input("Enter the right side of the triangle: "))
    left_side = float(input("Enter the left side of the triangle: "))

    print(f"\nThe perimeter of the triangle is {top_side + right_side + left_side}.\n")


triangle_perimeter()